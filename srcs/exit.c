#include "../inc/sh.h"

void	ft_exitshell(char **str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	if (i <= 1)
		exit(0);
	else
		ft_putendl_fd("exit: too many arguments\n", 2);
}
